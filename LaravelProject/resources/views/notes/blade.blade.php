Blade 模板引擎（官方文件翻譯與實作註解）
==========================================================================

1. 定義與特色
---------------------------------------------------------------------------
- Blade 是 Laravel 內建的簡潔且強大的模板引擎。
- 不像其他 PHP 模板引擎，Blade 不限制你在模板中使用原生 PHP。
- 所有 Blade 模板都會被編譯成純 PHP 並快取，只有檔案變動才會重新編譯，效能幾乎無損耗。
- Blade 檔案副檔名為 .blade.php，預設放在 resources/views 目錄。

2. 回傳 Blade 視圖
---------------------------------------------------------------------------
- 路由或控制器可用 view() 輔助函式回傳 Blade 視圖，並傳遞資料：
    Route::get('/', function () {
        return view('greeting', ['name' => 'Finn']);
    });
- 第二個參數為資料陣列，會自動轉成變數傳給 Blade 模板。

3. Blade 與原生 PHP
---------------------------------------------------------------------------
- Blade 模板內可直接寫 PHP 程式碼，不會有語法限制。
- Blade 會自動將模板編譯成 PHP，開發體驗與效能兼顧。

4. 進階：Livewire 動態增強
---------------------------------------------------------------------------
- 想讓 Blade 模板更動態、互動？可用 Laravel Livewire。
- Livewire 讓你用 Blade 寫元件，並加上即時互動功能（如 React/Vue），但不用寫 JS、也不用前端 build。
- 適合需要現代互動前端但又想保有 Blade 開發體驗的專案。
- 官方文件：https://laravel-livewire.com/

5. 小結
---------------------------------------------------------------------------
- Blade 是 Laravel 預設、效能極佳的模板引擎，語法簡單、功能強大。
- 支援原生 PHP、條件、迴圈、元件、繼承等。
- 可搭配 Livewire 實現現代動態前端，無需複雜 JS 框架。
